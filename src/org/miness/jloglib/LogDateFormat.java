package org.miness.jloglib;

/**
 * Represents the three log date formats SHORT, MEDIUM and LONG.
 * 
 * @author Michel Neeser
 * @version 1.0
 */
public enum LogDateFormat {
	
	/**
	 * The enum constant which represents the short date format.
	 */
	SHORT,
	
	/**
	 * The enum constant which represents the medium date format.
	 */
	MEDIUM,
	
	/**
	 * The enum constant which represents the long date format.
	 */
	LONG
	
}